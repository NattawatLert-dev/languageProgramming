public class A23for_loops{
    public static void main(String[] args){
        
        // for loop = execute some code a CERTAIN amount of times

        //Ex.1
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        System.out.println("########################");

        //Ex.2
        for (int i = 1; i <= 10; i+=3) {
            System.out.println(i);
        }

    }
}